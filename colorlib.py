"""
	Module contains a RGB color
	Class Variable

	rgb color from rapidtables.com
"""

class Color():
	pass

class Gray(Color):
	gainsboro = "#DCDCDC"
	lightgray = "#D3D3D3"
	darkgray = "#A9A9A9"
	gray = "#808080"
	dimgray = "#696969"
	lightslategray = "#778899"
	slategray = "#708090"
	darkslategray = "#2F4F4F"
class White(Color):
	white = "FFFFFF"
	snow = "#FFFAFA"
	honeydew = "#F0FFF0"
	mintcream = "#F5FFFA"
	azure = "#F0FFFF"
	aliceblue = "#F0F8FF"
	ghostwhite = "#F8F8FF"
	whitesmoke = "#F5F5F5"
	seashell = "#FFF5EE"
	beige = "#F5F5DC"
	oldlace = "#FDF5E6"
class Red(Color):
	red = "#FF0000"
	darkred = "#8B0000"
	marron = "#800000"
	tomato = "#FF6347"
	firebrick = "#B22222"
	crimson = "#DC143C"